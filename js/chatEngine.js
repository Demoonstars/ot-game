/* =========================================================
   ФАЙЛ: js/chatEngine.js
   Генератор случайных сообщений от сотрудников
========================================================= */

const ChatEngine = (() => {
    let timer = null;
    let messageCounter = 2; // Начинаем со 2, так как ID 1 уже есть у Юлии Борисовны

    // База отправителей
    const senders = [
        "Охранник КПП", 
        "Сантехник Михалыч", 
        "Главный инженер", 
        "Старшая Горничная", 
        "Шеф-повар", 
        "Бухгалтерия", 
        "Снабжение",
        "Айтишник Влад"
    ];

    // База случайных событий и сплетен
    const texts = [
        "Тут Михалыч опять без страховки на крышу лезет... Сними его оттуда!",
        "А когда новую партию перчаток выдадут? Мои уже до дыр стерлись.",
        "В подвале вода капает прямо на электрощиток. Это нормально или вырубаем?",
        "Кто-то огнетушителем пожарную дверь подпер, чтобы не закрывалась. Опять штраф получим.",
        "Проверь аптечку в горячем цеху, там бинты кончились и перекись кто-то вылил.",
        "Гости жалуются на жуткий запах краски на 4 этаже. Подрядчики говорят, что краска ЭКО.",
        "А плановый инструктаж точно сегодня? Может на пятницу перенесем, а то у нас банкет?",
        "У нас датчик дыма на кухне опять пищит просто так. Я его скотчем заклеил пока.",
        "Можно мне отгул на завтра? Я палец дверью прищемил, производственную травму оформлять не хочу...",
        "Влад из IT провода поперек коридора кинул. Я уже два раза споткнулась!",
        "На заднем дворе кто-то курил возле газовых баллонов. Я не разглядел кто.",
        "Зарплату задерживают на день. Народ бунтует, говорят, работать не выйдут."
    ];

    // Запуск фонового генератора
    const start = (onNewMessage) => {
        if (timer) clearTimeout(timer);
        
        const scheduleNext = () => {
            // Случайная задержка от 15 до 45 секунд
            const delay = Math.random() * 30000 + 15000; 
            
            timer = setTimeout(() => {
                const from = senders[Math.floor(Math.random() * senders.length)];
                const text = texts[Math.floor(Math.random() * texts.length)];
                
                // Отправляем новое сообщение через коллбэк
                onNewMessage({ 
                    id: messageCounter++, 
                    from: from, 
                    text: text, 
                    read: false 
                });
                
                scheduleNext(); // Планируем следующее сообщение
            }, delay);
        };
        
        scheduleNext();
    };

    // Остановка генератора (при окончании игры или паузе)
    const stop = () => {
        if (timer) clearTimeout(timer);
    };

    return { start, stop };
})();

// Экспортируем в глобальную область видимости
window.ChatEngine = ChatEngine;
